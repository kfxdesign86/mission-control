import * as React from 'react';
import { cn } from '@/lib/utils';

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { glow?: boolean; hover?: boolean }
>(({ className, glow = false, hover = true, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      'relative rounded-2xl border border-white/[0.06] bg-card backdrop-blur-xl',
      'shadow-card transition-all duration-300 ease-out',
      'bg-gradient-to-b from-white/[0.02] to-transparent',
      hover && 'hover:border-border-hover hover:shadow-card-hover hover:-translate-y-0.5',
      glow && 'hover:shadow-[0_8px_32px_-4px_rgba(0,0,0,0.5),0_0_30px_-5px_rgba(249,115,22,0.2)]',
      className
    )}
    {...props}
  />
));
Card.displayName = 'Card';

